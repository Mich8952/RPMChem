def findTextBetweenMarkers(text, marker="PROBLEMS") :
  out = ""
  sections = []
  firstSplitArray = re.split("@----" + marker + " START----@", text)
  for upperSection in firstSplitArray :
    section = re.split("@----" + marker + " END----@", upperSection)
    if len(section) != 1 :
      sections.append(section[0])
  for section in sections :
    out += section
  return out

