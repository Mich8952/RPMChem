import orjsonl

class arraysToJSONL :

  def __init__(self, problemsArray, solutionsArray, spacer="-") :
    self.questions = {}
    for problem in problemsArray :
      id = self.re.search("[1-9][0-9]*" + spacer + "[1-9][0-9]*", problem)
      if id != None :
        problem = problem[id.end() + 1:]
        id = id.group(0)
        self.questions[id] = [problem]
    for solution in solutionsArray :
      id = self.re.search("[1-9][0-9]*" + spacer + "[1-9][0-9]*", solution)
      if id != None :
        solution = solution[id.end() + 1:]
        id = id.group(0)
        if id in self.questions :
          self.questions[id].append(solution)
    self.JSONarray = self.__getJSONL()

  def __getJSONL(self) :
    out = []
    for id in self.questions :
      if len(self.questions[id]) == 2 and self.questions[id][0] != '' and self.questions[id][1] != '' :
        JSON = {
            "prompt" : self.questions[id][0],
            "completion" : self.questions[id][1]
        }
        out.append(JSON)
    return out

  def saveJSONL(self, filename) :
    self.orjsonl.save(filename, self.JSONarray)

  def showJSONL(self) :
    for JSON in self.JSONarray :
      print(JSON)

